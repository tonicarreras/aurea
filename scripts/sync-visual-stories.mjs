#!/usr/bin/env node
/**
 * Generates visual-story-manifest.ts from COMPONENT_MATURITY stable slugs + *.stories.ts exports.
 * Run: node scripts/sync-visual-stories.mjs
 */
import { spawnSync } from 'node:child_process';
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join } from 'node:path';

const root = join(process.cwd(), 'projects/components');
const libRoot = join(root, 'src/lib');
const maturityPath = join(libRoot, 'component-maturity.ts');
const overridesPath = join(root, 'e2e-visual/visual-story-overrides.json');
const outPath = join(root, 'e2e-visual/visual-story-manifest.ts');

/** Story export name overrides when Default is missing or a variant is preferred. */
const DEFAULT_OVERRIDES = {
  badge: 'Variants',
  button: 'Primary',
  message: 'Success',
  progress: 'Determinate',
  skeleton: 'Text',
  popover: 'Filters',
  'form-field': 'WithInput',
};

function readStableSlugs() {
  const src = readFileSync(maturityPath, 'utf8');
  const slugs = [];
  const blockStart = /^\s{2}(?:'([^']+)'|([\w-]+)):\s*\{/;
  const singleStable = /^\s{2}(?:'([^']+)'|([\w-]+)):\s*\{\s*level:\s*'stable'/;
  const levelStable = /level:\s*'stable'/;

  let pendingSlug = null;
  for (const line of src.split('\n')) {
    const single = line.match(singleStable);
    if (single) {
      slugs.push(single[1] ?? single[2]);
      pendingSlug = null;
      continue;
    }
    const start = line.match(blockStart);
    if (start) {
      pendingSlug = start[1] ?? start[2];
    }
    if (pendingSlug && levelStable.test(line)) {
      slugs.push(pendingSlug);
      pendingSlug = null;
    }
  }
  return slugs.sort();
}

function readStoryTitle(storiesPath) {
  const content = readFileSync(storiesPath, 'utf8');
  const match = content.match(/title:\s*['"]([^'"]+)['"]/);
  if (!match) {
    throw new Error(`No title in ${storiesPath}`);
  }
  return match[1];
}

/** Storybook story id prefix from meta title (e.g. `Aurea/InputDate` → `aurea-inputdate`). */
function titleToStoryIdPrefix(title) {
  return title.toLowerCase().replace(/\s+/g, '-').replace(/\//g, '-');
}

function exportNameToKebab(name) {
  return name
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/([A-Z])([A-Z][a-z])/g, '$1-$2')
    .toLowerCase();
}

function findStoriesFile(slug) {
  const dir = join(libRoot, slug);
  for (const name of readdirSync(dir)) {
    if (name.endsWith('.stories.ts')) {
      return join(dir, name);
    }
  }
  return null;
}

function listStoryExports(storiesPath) {
  const content = readFileSync(storiesPath, 'utf8');
  const exports = [];
  for (const match of content.matchAll(/^export const (\w+): Story/gm)) {
    exports.push(match[1]);
  }
  return exports;
}

function resolveStoryExport(slug, exports, fileOverrides) {
  const preferred = fileOverrides[slug] ?? DEFAULT_OVERRIDES[slug];
  if (preferred && exports.includes(preferred)) {
    return preferred;
  }
  if (exports.includes('Default')) {
    return 'Default';
  }
  return exports[0] ?? null;
}

function loadOverrides() {
  try {
    return JSON.parse(readFileSync(overridesPath, 'utf8'));
  } catch {
    return {};
  }
}

function main() {
  const fileOverrides = loadOverrides();
  const slugs = readStableSlugs();
  const entries = [];

  for (const slug of slugs) {
    const storiesPath = findStoriesFile(slug);
    if (!storiesPath) {
      console.warn(`Skip ${slug}: no *.stories.ts`);
      continue;
    }
    const exports = listStoryExports(storiesPath);
    const storyExport = resolveStoryExport(slug, exports, fileOverrides);
    if (!storyExport) {
      console.warn(`Skip ${slug}: no Story exports`);
      continue;
    }
    const title = readStoryTitle(storiesPath);
    const id = `${titleToStoryIdPrefix(title)}--${exportNameToKebab(storyExport)}`;
    entries.push({ name: `${slug}-${exportNameToKebab(storyExport)}`, id, slug });
  }

  const body = `/** Generated by scripts/sync-visual-stories.mjs — do not edit manually. */
export const VISUAL_STORIES: {
  readonly name: string;
  readonly id: string;
  readonly slug: string;
}[] = [
${entries.map((e) => `  { name: '${e.name}', id: '${e.id}', slug: '${e.slug}' },`).join('\n')}
];
`;

  writeFileSync(outPath, body);

  const format = spawnSync('bunx', ['prettier', '--write', outPath], {
    encoding: 'utf8',
    cwd: process.cwd(),
  });
  if (format.status !== 0) {
    console.error(format.stderr || format.stdout);
    process.exit(format.status ?? 1);
  }

  console.log(`Wrote ${entries.length} visual stories → ${outPath}`);
}

main();
