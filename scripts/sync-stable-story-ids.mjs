#!/usr/bin/env node
/**
 * Regenerates stable-story-ids.ts from COMPONENT_MATURITY stable slugs.
 * Run: node scripts/sync-stable-story-ids.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const maturityPath = join(process.cwd(), 'projects/components/src/lib/component-maturity.ts');
const outPath = join(process.cwd(), 'projects/components/.storybook/stable-story-ids.ts');

function slugToStoryPrefix(slug) {
  if (slug === 'input-text') {
    return 'aurea-inputtext--';
  }
  return `aurea-${slug}--`;
}

function readStableSlugs() {
  const src = readFileSync(maturityPath, 'utf8');
  const slugs = [];
  const singleStable = /^\s{2}(?:'([^']+)'|([\w-]+)):\s*\{\s*level:\s*'stable'/;
  const blockStart = /^\s{2}(?:'([^']+)'|([\w-]+)):\s*\{/;
  const levelStable = /level:\s*'stable'/;
  let pending = null;
  for (const line of src.split('\n')) {
    const single = line.match(singleStable);
    if (single) {
      slugs.push(single[1] ?? single[2]);
      pending = null;
      continue;
    }
    const start = line.match(blockStart);
    if (start) pending = start[1] ?? start[2];
    if (pending && levelStable.test(line)) {
      slugs.push(pending);
      pending = null;
    }
  }
  return slugs.sort();
}

const prefixes = readStableSlugs().map(slugToStoryPrefix).sort();

const body = `/**
 * Storybook story id prefixes for **stable** components (axe-core CI).
 * Generated by scripts/sync-stable-story-ids.mjs from COMPONENT_MATURITY — do not edit manually.
 */
export const STABLE_STORY_PREFIXES = [
${prefixes.map((p) => `  '${p}',`).join('\n')}
] as const;

export function isStableStoryId(storyId: string): boolean {
  return STABLE_STORY_PREFIXES.some((prefix) => storyId.startsWith(prefix));
}
`;

writeFileSync(outPath, body);
console.log(`Wrote ${prefixes.length} stable story prefixes → ${outPath}`);
